package dataStore;

public class DatamonStats {

	private int Level;
	private int HP;
	private int Attack;
	private int Defense;
	private int Speed;
	private int Rarity;
	
	public int getLevel() {
		return Level;
	}
	public void setLevel(int level) {
		Level = level;
	}
	public int getHP() {
		return HP;
	}
	public void setHP(int hP) {
		HP = hP;
	}
	public int getAttack() {
		return Attack;
	}
	public void setAttack(int attack) {
		Attack = attack;
	}
	public int getDefense() {
		return Defense;
	}
	public void setDefense(int defense) {
		Defense = defense;
	}
	public int getSpeed() {
		return Speed;
	}
	public void setSpeed(int speed) {
		Speed = speed;
	}
	public int getRarity() {
		return Rarity;
	}
	public void setRarity(int rarity) {
		Rarity = rarity;
	}
	
}